public class Variaveis {
    public static void main(String[] args) {
        // numericos
        int numero = 10;
        double preco = 4.4;
        int concat = 32;
        double casting = 1.5;
        int casted = (int) casting;
        System.out.println(numero);
        System.out.println(preco);
        System.out.println("Concatenacao: " + concat);
        System.out.println("casting double pra int: " + casted);

        //strings
        char letra = 'a'; //guarda unico carectere, segue a tabela unicode
        System.out.println(letra);
        char valor = 67;
        System.out.println(valor);

        String palavra = "alura cursos online";
        System.out.println(palavra);
    }
}
